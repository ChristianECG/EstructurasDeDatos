    private int validacionInt(String x){
        try{
            return Integer.parseInt(x);
        } catch (NumberFormatException ex){
            JOptionPane.showMessageDialog(this,x+" no es un valor entero. Rectifica");
        }
        return 0;
    }
    
    private byte validacionByte(String x){
        try{
            return Byte.parseByte(x);
        } catch (NumberFormatException ex){
            JOptionPane.showMessageDialog(this,x+" no es un valor entero. Rectifica");
        }
        return 0;
    }
    
    private void limpiar(){
        this.txf_Nombre.setText("");
        this.txf_Dia.setText("");
        this.txf_Mes.setText("");
        this.txf_Anio.setText("");
    }
    
